"""
Individual Variation Simulation Module.

Provides functionality to generate individuals with different cognitive parameters to simulate cognitive style diversity.
"""

import numpy as np
from typing import Dict, Any
import random
from config import BASE_PARAMETERS, VARIATION_RANGES


class IndividualVariation:
    """
    Individual Variation Simulator.

    Based on base parameters and variation ranges, generate personalized parameter sets for each individual,
    used to simulate diversity in cognitive styles (such as learning rate, forgetting rate, exploration tendency, etc.).

    Attributes
    ----------
    base_parameters : dict
        Base parameter values.
    variation_ranges : dict
        Variation ranges for each parameter.
    individual_parameters : dict
        Stores generated individual parameters, keyed by individual ID.
    """

    def __init__(self, base_parameters: Dict[str, Any] = None,
                 variation_ranges: Dict[str, Any] = None):
        """
        Initialize the individual variation simulator.

        Parameters
        ----------
        base_parameters : dict, optional
            Base parameters, defaults to config.BASE_PARAMETERS.
        variation_ranges : dict, optional
            Variation ranges, defaults to config.VARIATION_RANGES.
        """
        self.base_parameters = base_parameters or BASE_PARAMETERS
        self.variation_ranges = variation_ranges or VARIATION_RANGES
        self.individual_parameters = {}

    def generate_individual(self, individual_id: str):
        """
        Generate personalized parameters for a single individual.

        Parameters
        ----------
        individual_id : str
            Unique identifier for the individual.

        Returns
        -------
        dict
            Dictionary containing all parameters for this individual.
        """
        params = {}
        for param, base_value in self.base_parameters.items():
            if param in self.variation_ranges:
                variation = self.variation_ranges[param]
                if isinstance(variation, (int, float)):
                    # Relative proportion variation
                    min_val = base_value * (1 - variation)
                    max_val = base_value * (1 + variation)
                    params[param] = np.random.uniform(min_val, max_val)
                elif isinstance(variation, tuple) and len(variation) == 2:
                    # Absolute range variation
                    params[param] = np.random.uniform(variation[0], variation[1])
                else:
                    params[param] = base_value
            else:
                params[param] = base_value

        self.individual_parameters[individual_id] = params
        return params


def create_enhanced_individual_params(base_params):
    """
    Create enhanced individual parameters, adding additional parameters related to cognitive states.

    Parameters
    ----------
    base_params : dict
        Base parameter dictionary (generated by IndividualVariation).

    Returns
    -------
    dict
        Extended parameter dictionary, including cognitive state related parameters.
    """
    enhanced_params = base_params.copy()

    enhanced_params.update({
        'energy_variation': random.uniform(0.05, 0.15),
        'focus_bias': random.uniform(-0.1, 0.1),
        'exploration_bias': random.uniform(-0.1, 0.1),
        'fatigue_resistance': random.uniform(0.1, 0.3),
        'inspiration_frequency': random.uniform(0.05, 0.2)
    })

    return enhanced_params


